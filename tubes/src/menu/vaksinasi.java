    private void tabelbio(){
    String[] judul = {"nama","NIk","tanggal lahir","gender","telepon","alamat","tanggal vaksinasi","jenis vaksin","catatan"};
    model = new DefaultTableModel(judul,0);
    tvaksin.setModel(model);
    String sql = "select * from vaksinasi";
    
    try{
        rs = con.createStatement().executeQuery(sql);
        
        while(rs.next()){
            String nama=rs.getString("nama");
            String nik=rs.getString("nik");
            String tanggal_lahir=rs.getString("tanggal lahir");
            String gender=rs.getString("gender");
            String notelpn=rs.getString("no telpn");
            String alamat=rs.getString("alamat");
            String tgl_vaksin=rs.getString("tgl vaksin");
            String jenis_vaksin=rs.getString("jenis vaksin");
            String catatan=rs.getString("catatan");
            
            String[] data = {nama,nik,tanggal_lahir,gender,notelpn,alamat,tgl_vaksin,jenis_vaksin,catatan};
            model.addRow(data);
        }
    }catch(Exception e){
        System.out.println(e);
    }
    }
