    private void tvaksinMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tvaksinMouseClicked
        int i = this.tvaksin.getSelectedRow();
        
        String nama = (String) model.getValueAt(i,0);
        this.namalengkap.setText(nama);
        String nikk = (String) model.getValueAt(i,1);
        this.nik.setText(nikk);
        String tanggal_lahir = (String) model.getValueAt(i,2);
        this.ttl.setText(tanggal_lahir);
        gender.setSelectedItem(tvaksin.getValueAt(tvaksin.getSelectedRow(), 3).toString());
        String notelpn = (String) model.getValueAt(i,4);
        this.notelp.setText(notelpn);
        String alamt = (String) model.getValueAt(i,5);
        this.alamat.setText(alamt);
        String tangglvksn = (String) model.getValueAt(i,6);
        this.tglvaksin.setText(tangglvksn);
        jenisvaksin.setSelectedItem(tvaksin.getValueAt(tvaksin.getSelectedRow(), 7).toString());
        String cttn = (String) model.getValueAt(i,8);
        this.catatan.setText(cttn);
    
    }//GEN-LAST:event_tvaksinMouseClicked

